/**
 * Represents the keyboard input state.
 */
class Keyboard {
    // Properties to track key states
    LEFT = false;   // Left arrow key
    RIGHT = false;  // Right arrow key
    UP = false;     // Up arrow key
    DOWN = false;   // Down arrow key
    SPACE = false;  // Spacebar key
    KEY_D = false;  // D key
    KEY_F = false;  // F key
}
